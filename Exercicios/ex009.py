"""
Exercício Python 9: 
Faça um programa que leia um número Inteiro qualquer e mostre na tela a sua tabuada.
"""
valor = int(input("Por favor, digite o valor desejado: "))

print("O valor digitado é {0} e a sua tabuada é: \n {0}x1 = {1} \n {0}x2 = {2}\n {0}x3 = {3}\n {0}x4 = {4}\n {0}x5 = {5}\n {0}x6 = {6}\n {0}x7 = {7}\n {0}x8 = {8}\n {0}x9 = {9}\n {0}x10 = {10}".format(valor, valor*1, valor*2, valor*3, valor*4, valor*5, valor*6, valor*7, valor*8, valor*9, valor*10))