"""
Exercício Python 3: 
Crie um programa que leia dois números e mostre a soma entre eles.
"""

v1 = int(input("Por favor, digite o primeiro número: "))
v2 = int(input("Agora digite o segundo número por favor: "))

soma = v1 + v2

print("O resultado da soma entre {0} e {1} é: {2}".format(v1, v2, soma))